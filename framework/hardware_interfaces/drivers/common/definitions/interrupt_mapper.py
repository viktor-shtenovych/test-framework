def initialize_irq_mapper(mcu_type: MCUType) -> None:
    """
    ! Initialize IRQ mapper.

    Set current IRQ dictionary based on MCU type.

    @param mcu_type: MCU type.
    """
    global current_irq
    if mcu_type == MCUType.S32K148:
        current_irq = irq_s32k148
    elif mcu_type == MCUType.MPC5777C:
        current_irq = irq_mpc5777c
    else:
        raise ValueError(f"Unknown MCU type: {mcu_type}")


def irq_mapper(irq_id: int) -> str:
    """
    ! Map IRQ ID to IRQ name.

    @param irq_id: IRQ ID.
    @return: IRQ name.
    """
    return f"{current_irq.get(irq_id, 'UNKNOWN_IRQn')} : {irq_id}"
